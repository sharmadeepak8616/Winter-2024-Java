package Class2;

public class Datatypes {
    public static void main(String[] args) {

        /**
         * numbers
         *      whole
         *          (numeric data which can always be represented as without decimal-point)
         *          phone-number, zipcode, number-of-family-member, age
         *          Types of Java-variables which can store whole-number-values: byte, short, int(preferable), long
         *      decimal
         *          (numeric data which can be represented as with/without decimal-point)
         *          height, weight, distance
         *          Types of Java-variables which can store decimal-number-values: float, double(preferable)
         *
         * text
         *      single-character
         *          (any data which can be represented by single-character)
         *          math-symbols (+, -, *, /)
         *          Types of Java-variables which can store single-character-values: char
         *          To store any value as char, we need to put single-quotes ('*') around the value
         *
         *      multi-characters
         *          (any data which can be represented by single- or multi- characters)
         *          names, street-name
         *          Types of Java-variables which can store multi-characters-values: String
         *          To store any value as String, we need to put double-quotes ("Java Class 2") around the value
         *
         * yes/no
         *      true/false
         *          Types of Java-variables which can store true/false-values: boolean
         */

        /**
         * Java Variables or Java Datatypes
         *
         * -> We need variables in order to store data temporarily.
         * -> Types of variable and type of value MUST BE SAME.
         * -> Syntax to create a variable:
         *      datatype myVar;      <- created a variable(name=myVar) of type(datatype)
         *
         *      myVar = myValue;      <- using already created variable(myVar) to store value=myValue
         *
         *
         *      datatype myVar = myValue;   <- created a variable(name=myVar) of type(datatype); and storing value=myValue in variable(myVar)
         *
         */

        /**
         * Types of Datatypes in Java:
         *  1. Primitive (datatypes which are part of core-java)
         *      -> byte, short, int, long, float, double, char, boolean
         *  2. Non-Primitive (datatypes which added by Java-users to write better code)
         *      -> String, Array, ArrayList, HashSet, User-defined-Class etc
         */


    }
}
