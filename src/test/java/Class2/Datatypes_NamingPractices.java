public class Datatypes_NamingPractices {
    public static void main(String[] args) {

        /**
         * Rules and Good practices ro provide names:
         * 1. Variables:
         *      -> name cannot have spaces
         *      -> name should reflect the stored-value
         *      -> name should start with lowercase
         *      -> if name has more than 1 word, follow camelCaseFormat
         *      eg:
         *          my dog age -> mydogage -> myDogAge
         *          mike age -> mikeAge
         *
         * 2. Class
         *      -> name cannot have spaces
         *      -> name should reflect the code inside the Java-Class
         *      -> name should start with Uppercase
         *      -> name cannot start with digit or special character
         *      -> if name has more than 1 word, follow CamelCaseFormat
         *      eg:
         *         practice session -> Practicesession -> PracticeSession
         *
         * 3. Package
         *      -> name cannot have spaces
         *      -> name should reflect the Java-Class present in the Package
         *      -> name should start with Uppercase
         *      -> name cannot start with digit or special character
         *      -> if name has more than 1 word, follow CamelCaseFormat
         *      eg:
         *          my coding challenges -> Mycodingchallenges -> MyCodingChallenges
         */




    }
}
