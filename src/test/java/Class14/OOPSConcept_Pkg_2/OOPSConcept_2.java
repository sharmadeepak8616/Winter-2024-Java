package Class14.OOPSConcept_Pkg_2;

public class OOPSConcept_2 {
    /**
     * OOP Concept:
     *
     * 1. Encapsulation:
     *      Wrapping up the code in a class.
     *      To implement/practice Encapsulation concept, we create Class and write code in it.
     *
     * 2. Abstraction:
     *      Hiding unnecessary information from user.
     *      To implement/practice Abstraction concept, we create methods.
     *
     * 3. Inheritance
     *      -> To implement/practice Inheritance, we create parent-child relation between two classes.
     *      -> to create parent-child relation between two classes, we use "extends" keyword with Child-Class.
     *      -> childClass can access parentClass methods and variable as well.
     *      -> a childClass can extend ONLY one class (why? - Diamond Problem)
     *
     * 4. Polymorphism
     *      -> same name but different forms
     *      To implement/practice Polymorphism concept, we create multiple method with same name BUT different input-parameters.
     *      -> Types:
     *          1. Compile Time Polymorphism (Static Binding or Method Overloading)
     *          2. Run Time Polymorphism (Dynamic Binding or Method Overriding)
     *
     *          Compile Time Polymorphism (Static Binding or Method Overloading)
     *              When in a class, we create two or more methods with same name BUT different input-parameters.
     *              Ways to set different input-parameters:
     *                  1. different number of input-parameters
     *                  2. different datatype of input-parameters
     *                  3. different sequence of input-parameters
     *
     *          Run Time Polymorphism (Dynamic Binding or Method Overriding)
     *              -> can be practiced only in Inheritance set-up
     *              When we have methods SAME name and SAME input-parameters in ParentClass and ChildClass
     *              When we want to overwrite/override a ParentClass-method by ChildClass-method.
     *
     */
}
