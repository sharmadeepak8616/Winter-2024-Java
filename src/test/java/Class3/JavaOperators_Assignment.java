package Class3;

public class JavaOperators_Assignment {
    public static void main(String[] args) {
        /**
         * Java Operators:
         * Assignment (=)
         *
         * Assignment operators solves the right-hand side, then stores the result on left-hand side variable.
         */
        double billAmount = 12.86;
        System.out.println("Bill amount is $"+billAmount);

        billAmount = 34.67;
        System.out.println("Bill amount is $"+billAmount);



    }
}
